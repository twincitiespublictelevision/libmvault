<?php

namespace LibMVault\Result;

use LibMVault\PBSProfile;

/**
 * Class PBSProfileResult
 * @package LibMVault\Result
 */
class PBSProfileResult extends Result {

  /**
   * @param PBSProfile|null $value
   * @return PBSProfileResult
   */
  public static function ok($value): Result {
    return parent::ok($value); // TODO: Change the autogenerated stub
  }

  /**
   * @param \Exception $err
   * @return PBSProfileResult
   */
  public static function err(\Exception $err): Result {
    return parent::err($err); // TODO: Change the autogenerated stub
  }

  /**
   * @return PBSProfile|null
   * @throws \Exception
   */
  public function value() {
    if ($this->isError()) {
      throw $this->_e;
    }

    return $this->_value;
  }

  /**
   * @param mixed $fallback
   * @return PBSProfile|null|mixed
   */
  public function valueOr($fallback) {
    if ($this->isError()) {
      return $fallback;
    }

    return $this->_value;
  }
}